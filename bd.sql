CREATE TABLE actor ( --also room
id varchar(32) PRIMARY KEY,
free BOOLEAN[7][24] -- each hours of day of week (mon..sun)
);

CREATE TABLE piece (
id varchar(32) PRIMARY KEY,
roles JSON -- TODO:ADD ACTOR CONSTRAINT {"romeo":["tony","oscar"], "juliette":["binoche"]}
--title TEXT,
--description TEXT
--duration INTEGER -- let's say 1hour
--price INTEGER
);

CREATE TABLE play (
date date NOT NULL,-- to infere day of week => actor with available schedule
time time NOT NULL,-- to infere hour of day => actor with available schedule
piece_id varchar REFERENCES piece(id),
actors JSON
);


